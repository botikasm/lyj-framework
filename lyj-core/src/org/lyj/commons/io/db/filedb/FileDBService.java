package org.lyj.commons.io.db.filedb;

public class FileDBService {

    // ------------------------------------------------------------------------
    //                      f i e l d s
    // ------------------------------------------------------------------------

    private final FileDB _db;
    private final String _collection_name;

    // ------------------------------------------------------------------------
    //                      c o n s t r u c t o r
    // ------------------------------------------------------------------------

    public FileDBService(final FileDB db,
                         final String collection_name) {
        _db = db;
        _collection_name = collection_name;
    }

    // ------------------------------------------------------------------------
    //                      p u b l i c
    // ------------------------------------------------------------------------

    public FileDBCollection collection() {
        return _db.collection(_collection_name);
    }

    // ------------------------------------------------------------------------
    //                      p r o t e c t e d
    // ------------------------------------------------------------------------

    protected FileDB db() {
        return _db;
    }

    // ------------------------------------------------------------------------
    //                      p r i v a t e
    // ------------------------------------------------------------------------


}
