package org.lyj.desktopgap.app;

import org.lyj.desktopgap.app.http.HttpServer;

/**
 * Main Application Controller
 */
public class Application {

    // ------------------------------------------------------------------------
    //                      f i e l d s
    // ------------------------------------------------------------------------

    private HttpServer _web_server;

    // ------------------------------------------------------------------------
    //                      c o n s t r u c t o r
    // ------------------------------------------------------------------------

    public Application() {
        _web_server = new HttpServer();

    }

    // ------------------------------------------------------------------------
    //                      p u b l i c
    // ------------------------------------------------------------------------

    public void start() {
        _web_server.start();
    }

    public void stop() {
        _web_server.stop();
    }

    // ------------------------------------------------------------------------
    //                      p r i v a t e
    // ------------------------------------------------------------------------


}
