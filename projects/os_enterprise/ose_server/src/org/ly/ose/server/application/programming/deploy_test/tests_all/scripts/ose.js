    var instance = {};


    instance.callMember = function (namespace, func, params) {
        try {
            var response = $ose.callMember(namespace, func, params);
            return [
                response,
                params
            ];
        } catch (err) {
            console.error(FILE + '#callMember', err);
            return err;
        }
    };

    instance.recursive = function () {
        try {
            return $ose.callMember('tests.all', 'ose.recursive');
        } catch (err) {
            console.error(FILE + '#recursive', err);
            return err;
        }
    };

