(function () {

    'use strict';

    // -------------------------------------------------
    //          imports
    // -------------------------------------------------
    var $ = require('jquery');


    // -------------------------------------------------
    //          const
    // -------------------------------------------------



    // -------------------------------------------------
    //          costructor
    // -------------------------------------------------

    function ApiUtils() {}


    // -------------------------------------------------
    //          public
    // -------------------------------------------------

    ApiUtils.prototype.getVersion = function () {

        var version = "";

        $.ajax({
            url: "http://localhost:4000/xstep/utils/version/",
            dataType: "json",
            success: function(data) {
                version = data.response;
            },
            error: function(xhr, status, err) {
                version = err;
            }
        });
        return version;
    };




    // -------------------------------------------------
    //          private
    // -------------------------------------------------



    // -------------------------------------------------
    //          exports
    // -------------------------------------------------

    module.exports = ApiUtils;


}).call(this);